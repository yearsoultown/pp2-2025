a = 100
b = 5
c = 17

#addition (+)
print(a+b+c)

#subtraction (-)
print(a-b-c)

#multiplication (*)
print(c * b)

#division (/)
print(a/b)
print(a/c)

#modulus (%)
print(a % c)

#exponentiation (**)
print(a**b)

#floor division (//)
print(a//c)

# we can use these instead of writing longer (assignment operators)
